    <h2>Wat is en wat doet een timmerman specialist?</h2>
    <p>Wat is een timmerman?
Een timmerman is iemand die (delen van) bouwwerken construeert, repareert of monteert, waarbij het meestal gaat om houten constructies. Voorbeelden hiervan zijn kozijnen, dakconstructies, ramen, deuren, wanden en vloeren.
Binnen het vak van timmerman zijn er naast de ‘normale’ allround timmerman ook meer gespecialiseerde functies, bijvoorbeeld betontimmerman, meubelmaker en machinaal houtbewerker. Deze pagina betreft het vak van timmerman in het algemeen.
Wat doet een timmerman?
De werkzaamheden van timmermannen zijn vaak de volgende:
-   Repareren en monteren van deuren, vloeren, kozijnen, daken, trappen, plafonds, wanden, vensterbanken en/of andere (houten) constructies
-   Bestuderen van schetsen, tekeningen, bestekken en bouwplannen ter voorbereiding op het daadwerkelijke timmerwerk
-   Controleren van deugdelijkheid en conditie van bestaande constructies (met behulp van onder andere waterpas en winkelhaak)
-   Bepalen van afmetingen en afstanden met hulpmiddelen als meetlint, potlood, duimstok en krijt
-   Op maat zagen van houten platen en balken met (elektrisch) gereedschap
-   Aan elkaar bevestigen van bouwmaterialen met behulp van spijkers, schroeven, bouten etc.
-   Afwerken van aansluitingen en naden (bijvoorbeeld met plinten of kit)
-   Verrichten van onderhoudswerkzaamheden aan panden, zoals verhelpen van lekkage en aanbrengen van waterslagen
</p>
