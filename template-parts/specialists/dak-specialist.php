  <h2>   Wat is en wat doet een Dak specialist? </h2>  
  <br>  
<h4>   Dak specialist </h4>   

<p>     Een dak specialist is iemand die uw nieuwe dak kan realiseren, opknappen, onderhouden of repareren. Bij het realiseren moet u vooral denken aan een nieuw dak bijvoorbeeld een dakkapel. Bij het opknappen van het dak moet u denken aan schoonmaken, verven van het dakkapel of hier werkzaamheden aan uitvoeren die nodig zijn. Bij onderhoud moet u vooral denken aan het controleren van het dak en onderhoud hieraan doen. Denk hierbij bijvoorbeeld aan kapot of verouderd dakleer. Bij repareren moet u vooral denken aan alles wat reparatie nodig heeft omtrent het dak. </p>    
<h6>     De taken van een dak specialist zijn dan ook : </h6>    
<ul>   

<li>    De dak specialist regelt een planning en verzorgd deze ook. </li>    
<li>    De dak specialist zorgt voor de bouwmaterialen ( tenzij anders overeen gekomen in overeenkomst ). </li>    
<li>    De dak specialist regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn. </li>    
<li>    De dak specialist regelt een bouwvergunning voor u indien deze nodig is ( tenzij anders overeen gekomen in overeenkomst ). </li>    
<li>    De dak specialist regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ). </li>    
<li>    De dak specialist kan u adviseren</li>    
</ul>   

