<h4>  Klusbedrijf </h4>    
<p>       Daar zijn de meningen over verdeeld. Over het algemeen is een klussenbedrijf inzetbaar voor alle werkzaamheden in en rondom uw huis of pand. Denk hierbij aan klussen voor de badkamer, keuken, woonkamer of tuin en bestratingswerkzaamheden. Maar wat mag u verwachten van de klusjesman of vrouw en op basis waarvan kiest u ervoor om een klusbedrijf te benaderen? </p>       
<p>       Een klusjesman is iemand die u op allerlei verschillende manieren kan helpen. In de volksmond wordt de klusjesman ook wel een manusje-van-alles genoemd. Als het nou gaat om iets op te ruimen op uw zolder of een schilderij ophangen, de klusjesman of vrouw heeft dit zo gepiept. Een groot voordeel van het inhuren van een klusbedrijf is dat zij zeer flexibel zijn. Vaak wordt er van te voren een inschatting van de tijdsduur een bepaalde klus. Soms kan de klusjesman ook s ’avonds nog even langskomen als het om kleinere klusjes gaat. </p>        
<p>       De klusjesman beschikt over een grote tool-kit aan gereedschap. Met schroevendraaiers, boormachines en hamer en spijker is hij vrijwel altijd in staat een oplossing te bieden op uw gewenste klus. </p>       
 
<p>       Om u een beeld te geven kan een klusjesman u helpen bij…</p>       
<ul>         

<li>    het opruimen van een zolder of schuur</li>
<li>    het onderhouden van de tuin of snoeien</li>
<li>    timmerwerkzaamheden of schilder klussen</li>
<li>    het schoonmaken en legen van dakgoten</li>
<li>    het ontstoppen van de riolering</li>
<li>    behangen van een kamer</li>
<li>    het aansluiten of verplaatsen van apparatuur zoals vaatwassers of drogers</li>
</ul>       
 
<p>           De term klusjesman betekent iemand die allround inzetbaar is. Zo merken wij als schoonmaak en klussenbedrijf dat onze werknemers worden ingezet als tuinman, timmerman, stukadoor of metselaar. Binnen het werk van klusjesman vallen dus verschillende beroepen en functies uiteen. </p>           


<h6>           De taken van een klusbedrijf zijn dan ook : </h6>           
<li>    Het klusbedrijf regelt een planning en verzorgd deze ook. </li>
<li>    Het klusbedrijf zorgt voor de bouwmaterialen ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>    Het klusbedrijf verzorgd de juiste vakmannen voor uw klus per werkzaamheden. </li>
<li>    Het klusbedrijf regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn. </li>
<li>    Het klusbedrijf regelt een bouwvergunning voor u indien deze nodig is ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>    Het klusbedrijf regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>    Het klusbedrijf kan u adviseren. </li>
