<h3>   Wat is en wat doet een isolatie specialist</h3>    
<p>   Een isolatie specialist kan u adviseren over de soorten isolatie die mogelijk zijn ( bodem isolatie, dak isolatie, geluidsisolatie, kruipruimte isolatie, muur isolatie & vloer isolatie ). De isolatie specialist kan naast u adviseren deze werkzaamheden ook uitvoeren voor u. </p>     

<p>   De taken van een  isolatie specialist zijn dan ook : </p>     
<ul>   

<li>   De  isolatie specialist regelt een planning en verzorgd deze ook. </li >
<li>   De  isolatie specialist  zorgt voor de materialen ( tenzij anders overeen gekomen in overeenkomst ). </li >
<li>   De isolatie specialist regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn. </li >
<li>   De isolatie specialist regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ). </li >
<li>   De isolatie specialist kan u adviseren. </li >
</ul>      
