    <h2>Wat is en wat doet een tegelzetter specialist ?</h2>
    <br>
    <h4>Tegelzetter specialist</h4>
    <p>
Een tegelzetter is iemand die beroepsmatig wandtegels zet en vloertegels legt. Over het algemeen doet hij dit door de tegels in de mortel te drukken of door ze te lijmen met speciale tegellijm. Het beroep van tegelzetter is voortgekomen uit dat van metselaar.
Wat doet een tegelzetter?
De taken van tegelzetters zien er meestal als volgt uit:
</p>
<ul>
<li>Lezen en interpreteren van werktekeningen</li>
<li>Verzamelen van de benodige materialen, gereedschappen en hulpmiddelen</li>
<li>Meten en markeren van het te betegelen oppervlak</li>
<li>Op maat snijden van wand- en vloertegels</li>
<li>Aanmaken van lijm, mortel of specie</li>
<li>Schoonmaken en afvlakken van ondergrond</li>
<li>Eventueel voorstrijken van ondergrond met hechtmiddel</li>
<li>Aanbrengen van tegels op vloeren wanden</li>
<li>Eventueel aanbrengen van specialistisch tegelwerk (bijvoorbeeld mozaieken en andere speciale patronen, bijzondere rondingen of de betegeling van zwembaden) </li>
<li>Uitvoeren van herstelwerkzaamheden aan tegelwerk</li>
<li>Eventueel uitvoeren van sloop-, breek- en hakwerk</li>
<li>Eventueel begeleiden van een leerling-tegelzetter</li>
<li>Opruimen en schoonhouden van de werkplek</li>
</ul>


    <h6>De taken van een tegelzetter specialist zijn dan ook : </h6>
    <ul>
        <li>De tegelzetter specialist regelt een planning en verzorgd deze ook.</li>
        <li>De tegelzetter specialist zorgt voor de bouwmaterialen ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De tegelzetter specialist regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn.</li>
        <li>De tegelzetter specialist regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De tegelzetter specialist kan u adviseren.</li>

    </ul>

