<h2>  Wat is en wat doet een Mkb’er? </h2>       
<h3>    Een Mkb’er</h3>
<p>       Een Mkb’er is een onderneming die de verantwoordelijkheid op zich neemt om bouwactiviteiten te realiseren en te coördineren; de Mkb’er verzorgt, voor een in het contract bepaalde prijs en binnen een overeengekomen termijn, de levering van een volledig voltooid bouwwerk. De opdrachtgever of bouwheer kan op zijn beurt een architect of architectenbureau inschakelen, die het ontwerp en soms het toezicht op de bouwplaats voor zijn rekening neemt. Het ontwerp van de architect (of anderszins) resulteert vaak in een bestek en tekeningen waarin de beschrijving van het werk is opgenomen en hetgeen een onderlegger vormt voor en onderdeel is van de overeenkomst tussen opdrachtgever en Mkb’er. Het bestek en de tekeningen beschrijven zo nauwkeurig mogelijk wat de kwaliteits- en kwantiteitseisen zijn die de opdrachtgever aan het werk stelt. </p>           


<p>       De taken van een  Mkb’er zijn dan ook : </p>         
<ul>           
<li>    De  Mkb’er regelt een planning en verzorgd deze ook. </li>
<li>    De  Mkb’er  zorgt voor de bouwmaterialen ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>    De  Mkb’er  verzorgd de juiste vakmannen voor uw klus per werkzaamheden. </li>
<li>    De  Mkb’er  regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn. </li>
<li>    De  Mkb’er  regelt een bouwvergunning voor u indien deze nodig is ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>    De  Mkb’er  regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>    De Mkb’er kan u adviseren. </li>
</ul>       

