    <h2>  Wat is en wat doet een Kozijnen specialist? </h2>       
    <br>
<h5> kozijnen specialist </h5> 
<p>Een kozijnen specialist kan voor u het werk uit handen nemen wat betreft alles dat met een kozijn te maken kan hebben. Hij helpt u bij het plaatsen van een nieuw kozijn, hiervoor heeft hij dan wel eerst een maat voering nodig en zal hij dus eerst bij u komen meten. Het verschilt per kozijnen specialist of hij deze kozijnen zelf maakt, of deze opmaat laat maken door een ‘’ kozijnen boer’’. De kozijnen specialist kan het verf werk voor u doen, wij adviseren hierover te informeren bij de kozijnen specialist. Niet iedere kozijnen specialist kan dan ook deze werkzaamheden uitvoeren voor u. </p>
<p>Tegenwoordig zijn er een hoop verschillende soorten kozijnen op de markt, met een hele hoop leveranciers die allemaal hun eigen ‘’verbeteringen’’ hebben. Wil jij je kozijn vervangen hebben dan zal de specialist deze bij u komen opmeten daarna kunt u bij de specialist aangeven welk soort materiaal u wilt hebben. Zo kunt u kiezen uit een hout, kunststof of aluminium kozijn. Uiteraard hoort hier dan ook nog glas in. Geef ook goed aan wat voor soort glas u wilt ( wij adviseren om alles goed op papier vast te leggen zodat u nooit achteraf met verassing komt te staan. )  </p>

<p>De kozijnen specialist kan u ook adviseren over wat verstandig is, elke situatie kan zo zijn eigen voor en of nadelen hebben. </p>

<p>       De kozijnen specialist kan ook reparatie's uitvoeren, onderhoud plegen & het vernieuwen van het kozijn / glaswerk. </p>  
<ul>         
<li>      Bij reparaties kunt u vooral denken aan een klemmend raam, een deur die niet meer sluit, kapot hang en sluit werk, inbraak of brandschade & toch en vocht problemen. </li>           
<li>       Onderhoud kunt u bijvoorbeeld rekening houden met onderhouden van het kozijnen, bij werken dan wel opnieuw verven. Tocht weg halen indien er tocht is of betere bediening plaatsen en u adviseren over een optimale balans tussen isolatie en ventilatie. </li>           
<li>       Bij brandschade het kozijn te vernieuwen </li>
<li> Bij inbraak hang en slotwerk vervangen</li>           
</ul>




