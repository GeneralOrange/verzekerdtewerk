<h4>    Installatie specialist? </h4>   
<p>   De daadwerkelijke taken van een installateur hangen af van zijn specifieke functie en het bedrijf waarvoor hij werkt. In algemene zin kunnen de taken van installateurs er als volgt uit zien: </p>    
<ul>   
<li>   Installeren van (elektro)technische installaties aan de hand van schemaâ€™s en tekeningen</li>
<li>   Uitvoeren van controles en reparaties aan (elektro)technische installaties</li>
<li>   Opsporen en verhelpen van storingen aan installaties</li>
<li>   Voeren van overleg met klant en eventuele andere betrokkenen over de staat van de installatie(s) en de werkwijzen</li>
<li>   Rapporteren aan leidinggevende (schriftelijk en mondeling) over werkzaamheden, vorderingen en eventuele problemen</li>
<li>   Opsporen en melden van eventuele bedreigingen voor richtlijnen omtrent milieu en arbeidsomstandigheden, op basis van richtlijnen VCA (Veiligheid, gezondheid en milieu Checklist Aannemer) </li>
<li>   Indienen van verbetervoorstellen voor het functioneren van de installatie</li>
<li>   Signaleren en melden van eventueel meerwerk</li>
<li>   Schoonmaken en opruimen van de werkplek en afvoeren van afval</li>
</ul>   

<h6>   De taken van een  installatie specialist zijn dan ook : </h6>    
<ul>  
<li>   De installatie specialist regelt een planning en verzorgd deze ook. </li>
<li>   De installatie specialist zorgt voor de materialen ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>   De installatie specialist  verzorgd de juiste vakmannen voor uw klus per werkzaamheden. </li>
<li>   De installatie specialist  regelt de werkzaamheden van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>   De installatie specialist kan u adviseren.   </li>
</ul>   
