   <h2> Wat is en wat doet een  schoonmaak specialist? </h2>

<p>    Wat is een schoonmaker?
Een schoonmaker is iemand die beroepsmatig een ruimte schoonmaakt, van huizen en kantoorgebouwen tot openbare ruimtes. Een schoonmaker die binnen werkzaam is, wordt ook wel interieurverzorger of -verzorgster genoemd.
Wat doet een schoonmaker?
Schoonmakers verrichten schoonmaakwerkzaamheden in bijvoorbeeld huizen, kantoorruimtes, scholen, ziekenhuizen, uitgaan gelegenheden, hotels en op plekken in de openbare ruimte als pleinen, stoepen, stations en straten. </p>     
<p>    Afhankelijk van de plek waar ze werkzaam zijn, verrichten ze meestal meerdere van de volgende werkzaamheden: </p>     
<ul>   
-   <li>  De vloer vegen, dweilen en schrobben (eventueel met schrobmachine) </li>
<li>  Stofzuigen</li>
<li>  Meubilair afstoffen</li>
<li>  Meubilair poetsen met een natte doek</li>
<li>  De prullenbak legen en schoonmaken</li>
<li>  Keuken en sanitair reinigen</li>
<li>  Ramen en spiegels ontvetten en schoonmaken</li>
<li>  Shampooën van vloerbedekking</li>
<li>  Tegel voegsluier verwijderen</li>
<li>  Uiteraard kun je de schoonmakers zelf vragen waarin zij jou nog meer kun assisteren</li>
</ul>   
<p>    Schoonmakers in bijvoorbeeld fabrieken en industriële werkruimtes houden zich vaak bezig met het reinigen van machines en apparaten. Schoonmakers in hotels hebben naast de schoonmaakwerkzaamheden ook taken als het opmaken van bedden en het vervangen van handdoeken.
Voor schoonmakers die buiten werkzaam zijn, zijn er weer andere specifieke werkzaamheden als het prikken van vuil (papier, blikjes, peuken) en het vegen en reinigen van de straat (eventueel met hulpmiddelen als een hogedrukspuit of een schoonmaakkarretje). </p>     



<p>     De taken van een schoonmaak specialist zijn dan ook : </p>     
<ul>   
<li>  De schoonmaak specialist regelt een planning en verzorgd deze ook. </li>
<li>  De schoonmaak specialist zorgt voor de bouwmaterialen ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>  De schoonmaak specialist verzorgd de juiste vakmannen voor uw klus per werkzaamheden. </li>
<li>  De schoonmaak specialist regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn. </li>
<li>  De schoonmaak specialist regelt een bouwvergunning voor u indien deze nodig is ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>  De schoonmaak specialist regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ). </li>
<li>  De schoonmaak specialist kan u adviseren. </li>

</ul>   
