    <h2>   Wat is en wat doet een elektricien specialist? </h2>    
    <br>
<h4>   Elektricien specialist</h4>   

<p>  Een elektricien is gespecialiseerd in het aanleggen, (af)monteren en onderhouden van elektrische installaties. Dat kan elektra in huis zijn, zoals verlichting, meterkasten, intercom-, video-installaties en andere domotica. Maar er zijn ook elektriciens die zich richten op de aansturing van zware, elektrische machines in bijvoorbeeld fabrieken of transportmiddelen. Een elektricien zorgt ervoor dat stroom veilig afgetapt en elektrische apparaten en installaties veilig gebruikt kunnen worden. </p>  
<h5> Wat doet een elektricien? </h5>
<p>  Een elektricien is verantwoordelijk voor het aanleggen en aansluiten van allerlei typen elektriciteitsvoorzieningen en -installaties. Denk aan stopcontacten, groepenkasten en elektra-infrastructuur in woningen, kantoren, gebouwen, ziekenhuis en scholen. Een elektricien kan zich ook bezighouden met door elektra aangestuurde machines, bijvoorbeeld in fabrieken. Daarvoor zijn wel aanvullende cursussen en opleidingen nodig. Een elektricien komt eigenlijk op elke plek waar er elektra aan te pas komt. </p>  
<p>  Omdat het werken met stroom risico’s met zich meebrengt en niet goed aansluiten en onderhouden van elektra grote gevolgen kan hebben, werkt de elektricien volgen strenge regels en (NEN-)normen. Deze normen worden continu aangescherpt en up to date gehouden, ook omdat er nieuwe technieken bijkomen; bijscholing is daarom een belangrijk onderdeel binnen het takenpakket van een elektricien. </p>  
<p>  Onder de verantwoordelijkheden van een elektricien vallen samengevat doorgaans de volgende werkzaamheden: </p>  
<ul > 
<li>  Infrastructuur aanleggen voor elektriciteit</li>  
<li>  Trekken van elektriciteitskabels</li>  
<li>  Frezen van sleuven</li>  
<li>  Monteren van verlichting en bedrading</li>  
<li>  Installeren van groepenkasten</li>  
<li>  Afmonteren van stopcontacten en (aardlek)schakelaars</li>  
<li>  Testen van aangelegde elektra</li>  
<li>  Verhelpen en voorkomen van storingen</li>  
<li>  Preventief onderhoud aan elektrische installaties</li>  
</ul> 
<p>  Adviseren over energiebesparing en nieuwe, moderne toepassingen (denk aan automatische bediening van verlichting en geïntegreerde geluidsinstallaties) </p>  
<p>  Bij de aanleg van elektra op nieuwbouwlocaties moet de elektricien van tekeningen kunnen lezen. Hierop staan de plekken aangegeven waar aansluitpunten en stopcontacten moeten komen. </p>  
<h5> Laagspanning, middenspanning en hoogspanning</h5>
<p>  Als we het hebben over elektra in en rondom huizen, spreek je van laagspanning. De stroom die naar het laagspanningsnet wordt gevoerd, komt via een wijkcentrale. Dit is het middenspanningsnet, dat stroom krijgt aangevoerd via de hoogspanningsmasten die verspreid door het het land staan. Hoe hoger de spanning, hoe meer aanvullende kennis over het algemeen is vereist voor een elektricien. </p>  

    
<h6>De taken van een elektricien specialist zijn dan ook : </h6>    
<ul>  
<li>    De elektricien specialist regelt een planning en verzorgd deze ook.
<li>    De elektricien specialist zorgt voor de materialen ( tenzij anders overeen gekomen in overeenkomst ).
<li>    De elektricien specialist regelt de werkzaamheden van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ).
<li>    De elektricien specialist kan u adviseren.
</ul>   

