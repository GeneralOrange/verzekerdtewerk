<h4>   Elektricien specialist</h4>   
<p>Een elektricien is gespecialiseerd in het aanleggen, (af)monteren en onderhouden van elektrische installaties. Dat kan elektra in huis zijn, zoals verlichting, meterkasten, intercom-, video-installaties en andere domotica. Een elektricien zorgt ervoor dat stroom veilig afgetapt en elektrische apparaten en installaties veilig gebruikt kunnen worden. Een elektricien is verantwoordelijk voor het aanleggen en aansluiten van allerlei typen elektriciteitsvoorzieningen en -installaties. Denk aan stopcontacten, schakelaar & groepenkasten. </p>    
<h6>De taken van een elektricien specialist zijn dan ook : </h6>    
<ul>  
<li>    De elektricien specialist regelt een planning en verzorgd deze ook.
<li>    De elektricien specialist zorgt voor de materialen ( tenzij anders overeen gekomen in overeenkomst ).
<li>    De elektricien specialist regelt de werkzaamheden van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ).
<li>    De elektricien specialist kan u adviseren.
</ul>   

