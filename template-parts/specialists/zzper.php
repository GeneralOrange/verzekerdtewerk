    <h2>Wat is en wat doet een Zzp’er?</h2>
    <br>
    <h4>Zzp'er</h4>
    <p>Een zelfstandig ondernemer voert op basis van een overeenkomst, werkzaamheden uit voor verschillende opdrachtgevers. Hij werkt in opdracht, zonder dat er sprake is van een dienstbetrekking.</p>

    <p>Een zzp'er kan vaak meerdere specialismes beheersen. Zzp'ers zijn dan ook vaak multi inzetbaar en kunnen dan bijvoorbeeld het vervangen van een kozijn, schilderen hiervan, binnen netjes afwerken. U kunt bijvoorbeeld ook denken aan een kamer die u compleet wilt renoveren of een badkamer. Vaak kunnen zzp'ers dit in een eentje of met zijn 2en. </p> 

    <p>Een zzp'er kan vaak ook goed advies geven, veel zzp'ers hebben een hoop ervaring opgedaan in de loop der tijd en hebben dan ook veel meegemaakt en al gemaakt. Zij kunnen dan vaak situaties goed voor u inschatten wat betreft het aantal uur, kwa kosten en eventueel meer werk. Wij adviseren dan ook om altijd alles goed op papier te zetten zodat u niet voor onverwachten kosten komt te staan.</p>
    <h6>De taken van een Zzp’er zijn dan ook : </h6>
    <ul>
        <li>De Zzp’er regelt een planning en verzorgd deze ook.</li>
        <li>De Zzp’er zorgt voor de bouwmaterialen ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De Zzp’er verzorgd de juiste vakmannen voor uw klus per werkzaamheden.</li>
        <li>De Zzp’er regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn.</li>
        <li>De Zzp’er regelt een bouwvergunning voor u indien deze nodig is ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De Zzp’er regelt de verbouwing van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De Zzp’er kan u adviseren.</li>
    </ul>

