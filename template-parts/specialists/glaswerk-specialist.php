   <h2>   Wat is en wat doet een glaswerk specialist? </h2>    
<br>
<h4> Glaszet specialist</h4>
<p> Een glaszetter is een vakman die glas plaatst of vervangt in de woningbouw, hoogbouw, utiliteitsbouw, etc. Van woningen en kantoorgebouwen tot kassen, scholen, fabrieken en winkels. Soms werkt een glaszetter samen met en gezel glaszetter, die het werk voorbereidt en planningen maakt omtrent kosten en materialen.    </p>
<h3> Wat doet een glaszetter? </h3>
<p> De werkzaamheden van glaszetters zien er over het algemeen als volgt uit: </p>
<ul>
<li>Plaatsen van glazen ruiten (enkel, dubbel en speciaal glas) in ramen en kozijnen (beglazen), zowel bij bestaande gebouwen als bij nieuwbouw</li>
<li>Plaatsen van interieurglas, voor onder meer badkamers (douchedeuren en douchecabines), keukens, balustrades, trappen, spiegels, tafels, deuren en wanden</li>
<li>Opmeten en op maat snijden van enkel glas dan wel bestellen van dubbel glas bij de glashandel</li>
<li>Plannen en voorbereiden van de werkzaamheden</li>
<li>Veilig vervoeren van glas</li>
<li>Verwijderen van het oude glas, sponningen en kit</li>
<li>Herstellen van schade (bijvoorbeeld houtrot) aan kozijnen</li>
<li>Plaatsen van houten, kunstof, stalen of aluminium kozijnen</li>
<li>Plaatsen van afstandsblokjes, dichtingsbanden en glaslatten</li>
<li>Afkitten en afmessen van de kit</li>
<li>Opruimen van de werkplek en schoonmaken van ramen en kozijnen</li>
Belangrijke onderwerpen: functieomschrijving glaszetter, functie-eisen glaszetter, functieprofiel glaszetter, taken van een glaszetter, verantwoordelijkheden glaszetter, wat doet een glaszetter, wat is een glaszetter, werken als glaszetter, werkzaamheden glaszetter.
</ul>
<h3> Soorten glas</h3>
<p> Er zijn verschillende soorten glas waarmee glaszetters werken: </p>


<ul>
<li>Enkel glas
Eenvoudige, enkele ruiten. Soms wordt voor extra isolatie een voorzetraam geplaatst. </li>
 
<li>Dubbel glas
Minimaal twee glasplaten, waardoor betere isolatie plaatsvindt zowel wat betreft geluid als warmte. Bijzondere varianten zijn vacu√ºmglas, geluidsisolatie-glas en brand- of zonwerendglas. Soms wordt een speciale glassoort gebruikt die dankzij een speciale coating in combinatie met een spouwvulling van het gas argon of krypton nog beter isoleert, de zogenaamde Hoog Rendementsbeglazing: HR, HR+, HR++ en HR+++.</li>
 
<li>Gehard glas
Gehard glas is sterker dan normaal glas, doordat het onder een extra hoge temperatuur is gemaakt. Wanneer het breekt, valt het in zeer kleine stukjes uiteen. </li>
 
<li>Gelaagd glas en draadglas
Beide vormen zorgen voor extra stevigheid. Gelaagd glas bevat een tussenlaag van plastic, draadglas heeft een metalen rasterpatroon die voor extra kracht zorgt. </li>
 
<li>Gefigureerd glas en glas-in-lood
Onder gefigureerd glas vallen ruiten met bijvoorbeeld (gekleurde) teksten of versieringen erop. </li>
 
<li>Overige toepassingen
Verder heeft de glaszetter te maken met bijvoorbeeld lichtkoepels, glazen scheidingswanden, keukenwanden, monumentenglas, spiegels en (hard)glazen deuren. </li>
</ul>
 

