    <h2>Wat is en wat doet een stukadoor specialist?</h2>
    <br>
    <h4>Stukadoor specialist</h4>
    <p>
Een stukadoor is een vakman die stucwerk aanbrengt op muren, wanden, plafonds en gevels. Dit stucwerk, bestaande uit specie, pleister of gipsmortel, vormt een afwerklaag en is bedoeld ter bescherming en/of verfraaiing van het interieur.
Wat doet een stukadoor?
Stukadoors brengen een afwerklaag aan op muren, wanden, gevels en plafonds die dient ter versteviging, bescherming en ook vaak ter verfraaiing.
Tot de voornaamste werkzaamheden van de stukadoor behoren de volgende zaken:
</p>
<ul>
<li>Voorbereiden van het oppervlak (schoonmaken, verwijderen behang, etc.)
<li>Afplakken van overgangsgebieden met gaas ter voorkoming van scheuren
</li>
<li>Voorstrijken van de ondergrond met een voorstrijkmiddel
</li>
<li>Aanmaken (mixen) van de mortel
</li>
<li>Aanbrengen van de mortel op het oppervlak (het berapen of bepleisteren)
</li>
<li>Uitvlakkken met behulp van een rei 
</li>
<li>Glad maken van het oppervlak met een spackmes
</li>
<li>Eventueel bijschuren van oneffenheden met schuurgaas
</li>
<li>Reparen van oude stuklagen
</li>
    </ul>
    <h6>De taken van een stukadoor specialist zijn dan ook :  </h6>
    <ul>
        <li>De stukadoor specialist regelt een planning en verzorgd deze ook.</li>
        <li>De stukadoor specialist zorgt voor de materialen ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De stukadoor specialist regelt voor u de ontheffingen ( tenzij anders overeen gekomen in overeenkomst ) indien deze nodig zijn.</li>
        <li>De stukadoor specialist regelt de werkzaamheden van A tot Z. ( tenzij anders overeen gekomen in overeenkomst ).</li>
        <li>De stukadoor specialist kan u adviseren.</li>
    </ul>

